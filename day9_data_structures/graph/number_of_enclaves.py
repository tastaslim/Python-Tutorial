import sys
from typing import List

sys.setrecursionlimit(10 ** 8)


class Solution:

    def __dfs__(self, visited: List[List[int]], final_ans: List[List[int]], root, row, col, rows, columns):
        visited[row][col] = True
        nodes_to_visit = [(row - 1, col), (row + 1, col), (row, col + 1), (row, col - 1)]
        for (temp_row, temp_col) in nodes_to_visit:
            if 0 <= temp_row < rows and 0 <= temp_col < columns and not visited[temp_row][temp_col] and \
                    final_ans[temp_row][temp_col] == 1:
                self.__dfs__(visited, final_ans, root, temp_row, temp_col, rows, columns)

    def number_enclaves(self, grid: List[List[int]]) -> int:
        final_ans = [row[:] for row in grid]
        rows = len(grid)
        columns = len(grid[0])
        visited_arr = [[False for _ in range(columns)] for _ in range(rows)]
        # To handle all 1 cases in case of large numbers to avoid max recursion depth
        has_zero = False
        for m in range(rows):
            for n in range(columns):
                if final_ans[m][n] == 0:
                    has_zero = True
        if not has_zero:
            return 0
        for row in range(rows):
            for col in range(columns):
                if not visited_arr[row][col] and grid[row][col] == 1 and (
                        row == rows - 1 or row == 0 or col == 0 or col == columns - 1):
                    self.__dfs__(visited_arr, final_ans, grid[row][col], row, col, rows, columns)
        ans = 0
        for i in range(rows):
            for j in range(columns):
                if final_ans[i][j] == 1 and not visited_arr[i][j]:
                    ans += 1
        return ans


